package hurufztoawhile;

public class HurufZtoAWhile {
    public static void main(String[] args) {
        System.out.println("Huruf Z - A Menggunakan Perulangan While:");

        char ch = 'Z';
        while (ch >= 'A') {
            System.out.print(ch + " ");
            ch--;
        }
    }
}

