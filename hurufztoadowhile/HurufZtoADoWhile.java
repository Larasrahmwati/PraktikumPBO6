package hurufztoadowhile;

public class HurufZtoADoWhile {
    public static void main(String[] args) {
        System.out.println("Huruf Z - A menggunakan do while:");

        char ch = 'Z';
        do {
            System.out.print(ch + " ");
            ch--;
        } while (ch >= 'A');
    }
}

  
